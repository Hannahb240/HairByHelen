    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try
        {
         //Set up the connection
         Connection con = DriverManager.getConnection("jdbc:mysql://localhost/hairbyhelen", "root", "2095ratlol");
         Statement stmt = (Statement) con.createStatement();
         Class.forName("com.mysql.jdbc.Driver");
         
         //Result set for purchases
         ResultSet purchasesRS = stmt.executeQuery("SELECT * FROM purchases ORDER BY purchase_id");
         
         // Sort out the auto-increment
         int greatestPurchaseID = 0;
         
         while (purchasesRS.next())
         {
             int currentPurchaseID = Integer.parseInt(purchasesRS.getString(5));
             if (currentPurchaseID > greatestPurchaseID)
                     {
                         greatestPurchaseID = currentPurchaseID;
                     }//if 
         }//while
         
         // Get the strings from the JTextFields
         String item  = itemTextField.getText();
         Double cost = Double.parseDouble(costTextField.getText());
         int receiptNo = Integer.parseInt(receiptTextField.getText());
         String date = datePurchasedTextField.getText();
         
         // query for MySQL
         String insert = "INSERT INTO purchases VALUES ('" + item + "'," +
                          cost + ", " + receiptNo +  ", '"
                          + date + "', " + (greatestPurchaseID + 1) + ")";
         System.out.println(insert);
         //execute MySQL instruction
         stmt.executeUpdate(insert);
         
        }//try
        
        catch(Exception e)
        {
            System.out.println(e);
        }//catch
    }//GEN-LAST:event_okButtonActionPerformed

